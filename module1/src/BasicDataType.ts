// ------- Basic Data Type use in TypeScript

// Primitive Data type-------

// ------- String

let firstname:string = 'Madina'

// ------ Number

let roll:number = 20

// ------ Boolean

let isAdmin:boolean = true

// ------- Null

let NameField:null = null

// ------ Undefined

let Myname:undefined = undefined


// Non Primitive Data Type 

// -------- Array 

let Friends : string[] = ['madina', 'Mina', 'tara']

Friends.push('madina')

let RollnumberList : number[] = [1,2,3]

RollnumberList.push(2)


// -------- Tuple 

// tuple --- array---order---type of values

let coordinator : [number, number] = [1,2]

let mixedup : [string, number] = ['madina', 22]

let ageName : [number, string, boolean] = [22, 'madina', true]